/*
    this programs explores how we can print numbers from various number systems like binary, hexadecima, to decimal
*/

public class number_systems 
{
    public static void main(String[] args) 
    {
        // binary
        int num1 = 0b1001;
        System.out.println(num1);

        int num2 = 0x8a;
        System.out.println(num2);
    }
    
}
