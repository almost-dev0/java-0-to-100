/*
    this programs explores how we can print numbers from various number systems like binary, hexadecima, to decimal
*/

public class number_systems 
{
    public static void main(String[] args) 
    {
        // binary
        int num1 = 0b1001;          //use prefix 0b
        System.out.println(num1);

        //hexadecimal
        int num2 = 0x8a;            //use prefix 0x
        System.out.println(num2);
    }
    
}
